package Q2b;

public class AddTwoInt {
	private int a;
	private int b;
	
	public AddTwoInt() {
		
	}
	
	public AddTwoInt(int a, int b) {
		this.a = a;
		this.b = b;
	}
	
	public int addTwoInt(int a, int b) {
		
		return a+b;
	}
	
	public int getSum() {
		
		return this.a + this.b;
	}
}
